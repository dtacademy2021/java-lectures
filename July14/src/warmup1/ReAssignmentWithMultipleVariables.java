package warmup1;

public class ReAssignmentWithMultipleVariables {

	public static void main(String[] args) {
		
		
		int x = 67;
		int y = 78;
		int z = 33;
		
		
		
		
		
		System.out.println(x);
		System.out.println(y);
		
		
		y = z;
		
		z = x;
		
		
		System.out.println(x);
		
		System.out.println(y);
		
		System.out.println(z);
		
		
		
		float myNumber = 1726721367627367L;
		
		
		long l = 3;
		
		double d = 45;
	
		
		//	byte > short > int > long > float > double 
		

		
		

	}

}
