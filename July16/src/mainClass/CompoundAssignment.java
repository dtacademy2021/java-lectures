package mainClass;

public class CompoundAssignment {

	public static void main(String[] args) {
		
		
		//Compound assignment operators
		int i = 8;
		
		i = i + 3;
		
		System.out.println(i);
		
		i += 5;  // i = i + 5;
		
		System.out.println(i);
		
		
		i -= 4;  // i = i - 4;
		
		System.out.println(i);
		
		
//		int numberOfStudentsInAClass =23;
//		
//		numberOfStudentsInAClass += 7;
		
		
		i *= 5; // i = i * 5;
		
		System.out.println(i);
		
		i /= 6;
		
		System.out.println(i);
		
		
		i  %= 3;
		
		System.out.println(i);
		
		
		
		String str = "Duotech";
		
		str += " Academy"; // str = str + " Academy";
		
		System.out.println(str);
		
		


		
		
	}

}
