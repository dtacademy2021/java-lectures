package loops;

public class LoopsDemo {

	public static void main(String[] args) {
		// Print "Hi" 100 times
		
		int counter = 0;
		
		while(counter < 100) {
			System.out.print("Hi: ");
			System.out.println(counter);
			counter++;
			
		}
		
		System.out.println(counter);
		
		
		
		
		
		
		

	}

}
